public class Objet {
	
	private String nom;
	
	public Objet(String nom) {
		this.nom = nom;
	}
	
	/**
	* Methode qui permet de récupérer 
	* le nom de l'objet
	* 
	* @return le nom du Pirate
	*/
	public String getNomObjet() {
		return nom;
	}
	
}












